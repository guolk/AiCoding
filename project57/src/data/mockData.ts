const createIngredient = (
  name: string,
  amount: number,
  unit: string,
  category: IngredientCategory,
  nutritionPer100g?: { calories: number; protein: number; fat: number; carbs: number }
): Ingredient => ({
  id: generateId(),
  name,
  amount,
  unit,
  category,
  nutrition: nutritionPer100g ? {
    calories: (nutritionPer100g.calories * amount) / 100,
    protein: (nutritionPer100g.protein * amount) / 100,
    fat: (nutritionPer100g.fat * amount) / 100,
    carbs: (nutritionPer100g.carbs * amount) / 100,
  } : undefined,
});

const createStep = (order: number, instruction: string, duration?: number, tips?: string): RecipeStep => ({
  id: generateId(),
  order,
  instruction,
  duration,
  tips,
});

